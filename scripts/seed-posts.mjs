async function deleteAll() {
  console.log('🗑  Удаляю старые статьи...')
  const ids = await client.fetch('*[_type == "post"]._id').catch(() => [])
  if (!ids.length) { console.log('   Статей нет.\n'); return }
  for (const id of ids) await client.delete(id).catch(() => {})
  console.log(`   Удалено: ${ids.length}\n`)
}

async function uploadImage(url) {
  try {
    process.stdout.write(' 📸')
    const buf = await fetchImage(url)
    const asset = await client.assets.upload('image', buf, { filename: 'cover.jpg', contentType: 'image/jpeg' })
    return { _type: 'image', asset: { _type: 'reference', _ref: asset._id } }
  } catch { return null }
}

