async function deleteAll() {
  console.log('🗑  Удаляю старые статьи...')
  const ids = await client.fetch('*[_type == "post"]._id')
  if (!ids.length) { console.log('   Статей нет.'); return }
  for (const id of ids) { await client.delete(id).catch(() => {}) }
  console.log(`   Удалено: ${ids.length}\n`)
}

async function uploadImage(url) {
  try {
    process.stdout.write(' 📸')
    const buf = await fetchImage(url)
    const asset = await client.assets.upload('image', buf, { filename: 'cover.jpg', contentType: 'image/jpeg' })
    return { _type: 'image', asset: { _type: 'reference', _ref: asset._id } }
  } catch { return null }
}

async function seed() {
  await deleteAll()
  console.log(`✍️  Создаю ${POSTS.length} статей...\n`)
  let ok = 0
  for (const p of POSTS) {
    process.stdout.write(`📝 ${p.title.slice(0,48)}...`)
    const coverImage = await uploadImage(p.photo)
    await client.create({
      _type: 'post', title: p.title,
      slug: { _type: 'slug', current: p.slug },
      publishedAt: p.publishedAt, author: 'Александра Серова',
      tag: p.tag, readTime: p.readTime, excerpt: p.excerpt,
      ...(coverImage && { coverImage }),
      body: toBlocks(p.body),
    })
    console.log(' ✅')
    ok++
    await new Promise(r => setTimeout(r, 400))
  }
  console.log(`\n✨ Готово! Создано ${ok} статей.\n`)
}

seed()
