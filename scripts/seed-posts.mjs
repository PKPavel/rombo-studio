async function uploadImage(url) {
  try {
    console.log(`  📸 Загружаю фото...`)
    const buf = await fetchImage(url)
    const asset = await client.assets.upload('image', buf, {
      filename: 'cover.jpg',
      contentType: 'image/jpeg',
    })
    return { _type: 'image', asset: { _type: 'reference', _ref: asset._id } }
  } catch (e) {
    console.log(`  ⚠️  Фото не загрузилось: ${e.message}`)
    return null
  }
}

async function seed() {
  console.log(`\n🚀 Создаю ${POSTS.length} статей в Sanity...\n`)
  let ok = 0, fail = 0

  for (const post of POSTS) {
    try {
      process.stdout.write(`📝 ${post.title.slice(0, 50)}...`)
      const coverImage = await uploadImage(post.photo)
      const doc = {
        _type: 'post',
        title: post.title,
        slug: { _type: 'slug', current: post.slug },
        publishedAt: post.publishedAt,
        author: 'Александра Серова',
        tag: post.tag,
        readTime: post.readTime,
        excerpt: post.excerpt,
        ...(coverImage && { coverImage }),
        body: [{ _type: 'block', _key: Math.random().toString(36).slice(2,9), style: 'normal', markDefs: [], children: [{ _type: 'span', _key: Math.random().toString(36).slice(2,9), text: post.body, marks: [] }] }],
      }
      await client.create(doc)
      console.log(' ✅')
      ok++
    } catch (e) {
      console.log(` ❌ ${e.message}`)
      fail++
    }
    await new Promise(r => setTimeout(r, 300))
  }
  console.log(`\n✨ Готово! Создано: ${ok}, ошибок: ${fail}\n`)
}

seed()
